<?php

use Laravel\Lumen\Testing\DatabaseMigrations;
use Laravel\Lumen\Testing\DatabaseTransactions;

class ConverterServiceTest extends TestCase {

    private $converter;

    protected function setUp(): void {
        parent::setUp(); // TODO: Change the autogenerated stub
        $this->converter = new \App\Services\ConverterService();
    }

    //    0  0   |   10  a   |   20  k   |   30  u   |   40  E   |   50  O   |    60  Y
    //    1  1   |   11  b   |   21  l   |   31  v   |   41  F   |   51  P   |    61  Z
    //    2  2   |   12  c   |   22  m   |   32  w   |   42  G   |   52  Q   |
    //    3  3   |   13  d   |   23  n   |   33  x   |   43  H   |   53  R   |
    //    4  4   |   14  e   |   24  o   |   34  y   |   44  I   |   54  S   |
    //    5  5   |   15  f   |   25  p   |   35  z   |   45  J   |   55  T   |
    //    6  6   |   16  g   |   26  q   |   36  A   |   46  K   |   56  U   |
    //    7  7   |   17  h   |   27  r   |   37  B   |   47  L   |   57  V   |
    //    8  8   |   18  i   |   28  s   |   38  C   |   48  M   |   58  W   |
    //    9  9   |   19  j   |   29  t   |   39  D   |   49  N   |   59  X   |

    /** @test */
    public function expected_dec_is_return_for_hash() {
        $this->assertEquals( 0, $this->converter->toDec( "0" ) );
        $this->assertEquals( 1, $this->converter->toDec( "1" ) );
        $this->assertEquals( 9, $this->converter->toDec( "9" ) );
        $this->assertEquals( 10, $this->converter->toDec( "a" ) );
        $this->assertEquals( 34, $this->converter->toDec( "y" ) );
        $this->assertEquals( 36, $this->converter->toDec( "A" ) );
        $this->assertEquals( 61, $this->converter->toDec( "Z" ) );
        $this->assertEquals( 62, $this->converter->toDec( "10" ) );
        $this->assertEquals( 453, $this->converter->toDec( "7j" ) );
        $this->assertEquals( 238328, $this->converter->toDec( "1000" ) );
        $this->assertEquals( 925164, $this->converter->toDec( "3SG0" ) );
    }

    /** @test */
    public function expected_hash_is_return_for_dec() {
        $this->assertEquals( "0", $this->converter->toDuosexagesimal( 0 ) );
        $this->assertEquals( "1", $this->converter->toDuosexagesimal( 1 ) );
        $this->assertEquals( "5", $this->converter->toDuosexagesimal( 5 ) );
        $this->assertEquals( "9", $this->converter->toDuosexagesimal( 9 ) );
        $this->assertEquals( "a", $this->converter->toDuosexagesimal( 10 ) );
        $this->assertEquals( "k", $this->converter->toDuosexagesimal( 20 ) );
        $this->assertEquals( "p", $this->converter->toDuosexagesimal( 25 ) );
        $this->assertEquals( "y", $this->converter->toDuosexagesimal( 34 ) );
        $this->assertEquals( "A", $this->converter->toDuosexagesimal( 36 ) );
        $this->assertEquals( "B", $this->converter->toDuosexagesimal( 37 ) );
        $this->assertEquals( "J", $this->converter->toDuosexagesimal( 45 ) );
        $this->assertEquals( "U", $this->converter->toDuosexagesimal( 56 ) );
        $this->assertEquals( "Z", $this->converter->toDuosexagesimal( 61 ) );
        $this->assertEquals( "10", $this->converter->toDuosexagesimal( 62 ) );
        $this->assertEquals( "1Z", $this->converter->toDuosexagesimal( 123 ) );
        $this->assertEquals( "3V", $this->converter->toDuosexagesimal( 243 ) );
        $this->assertEquals( "7j", $this->converter->toDuosexagesimal( 453 ) );
        $this->assertEquals( "3SG0", $this->converter->toDuosexagesimal( 925164 ) );
    }
}
